**Kim jest prekariat?**

Prekariat to neologizm złożony z słów "precarious" (niepewny) i "proletariat". Opisuje on rosnącą grupę pracowników, którzy:
- Nie mają stałego zatrudnienia
- Pozbawieni są zabezpieczeń społecznych
- Żyją w ciągłej niepewności ekonomicznej
- Wykonują prace poniżej swoich kwalifikacji

**Mechanizmy prekaryzacji**

Współczesna gospodarka wykorzystuje różne mechanizmy do tworzenia prekariatu:

1. **Outsourcing** - przenoszenie działalności do zewnętrznych firm
2. **Umowy śmieciowe** - zatrudnienie na umowy-zlecenie bez ochrony socjalnej
3. **Gig economy** - fragmentaryzacja pracy w drobne zlecenia
4. **Sztuczne samozatrudnienie** - zmuszanie do prowadzenia jednoosobowych firm

**Koszty społeczne**

Prekaryzacja pracy niesie poważne konsekwencje:
- Wzrost nierówności społecznych
- Obniżenie poziomu życia
- Problemy demograficzne (odkładanie decyzji o potomstwie)
- Niestabilność polityczna

**Rola korporacji**

Duże korporacje systematycznie wykorzystują prekaryzację do:
- Obniżania kosztów pracy
- Unikania odpowiedzialności pracodawcy
- Maksymalizacji zysków kosztem pracowników
- Przerzucania ryzyka biznesowego na jednostki

**Przykład platform cyfrowych**

Platformy takie jak Uber czy Deliveroo przedstawiają się jako technologiczne, podczas gdy de facto są agencjami pracy wykorzystującymi luki prawne do unikania odpowiedzialności za pracowników.

**Opór i alternatywy**

Przeciwko prekaryzacji powstają różne formy oporu:
