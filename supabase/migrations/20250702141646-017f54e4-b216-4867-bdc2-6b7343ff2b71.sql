**Czym jest kapitał społeczny?**

Kapitał społeczny to sieć powiązań między ludźmi w społeczeństwie, która umożliwia efektywne funkcjonowanie tego społeczeństwa. Składa się z zaufania, norm społecznych i sieci obywatelskich, które ułatwiają koordynację i współpracę.

**Bonding vs Bridging**

Socjolodzy wyróżniają dwa typy kapitału społecznego:
- **Bonding** (łączący) - więzi w ramach jednorodnych grup społecznych
- **Bridging** (pomostowy) - połączenia między różnymi grupami społecznymi

Zdrowe społeczeństwo potrzebuje obu typów, ale szczególnie ważny jest kapitał pomostowy, który łączy różne grupy i zapobiega polaryzacji.

**Kryzys zaufania w Polsce**

Badania pokazują, że Polacy mają jeden z najniższych poziomów zaufania społecznego w Europie. Tylko około 20% Polaków uważa, że większości ludzi można ufać - to znacznie mniej niż w krajach skandynawskich, gdzie wskaźnik ten przekracza 60%.

**Konsekwencje niskiego zaufania**

Niski poziom zaufania prowadzi do:
- Ograniczonej współpracy społecznej
- Słabszego rozwoju ekonomicznego
- Mniejszej efektywności instytucji publicznych
- Wzrostu kosztów transakcyjnych w gospodarce

**Źródła kryzysu**

Przyczyny niskiego zaufania w Polsce to między innymi:
- Doświadczenia historyczne (wojny, okupacje, system komunistyczny)
- Szybkie zmiany społeczne po 1989 roku
- Polaryzacja polityczna
- Słaba jakość instytucji publicznych

**Odbudowa zaufania**

Budowanie kapitału społecznego to proces długoterminowy, który wymaga:
- Wzmocnienia instytucji demokratycznych
- Promocji dialogu międzygrupowego
- Inwestycji w edukację obywatelską
- Zwiększenia transparentności władzy publicznej

